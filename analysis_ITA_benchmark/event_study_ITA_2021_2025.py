def extract_close_prices(raw: pd.DataFrame, tickers: list[str]) -> pd.DataFrame:
    """Convert yfinance output into date/ticker/close long format."""
    close_frames = []

    if raw.empty:
        raise ValueError("Yahoo Finance returned no price data.")

    if isinstance(raw.columns, pd.MultiIndex):
        level_0 = set(raw.columns.get_level_values(0))
        level_1 = set(raw.columns.get_level_values(1))

        for ticker in tickers:
            close_series = None
            if ticker in level_0 and "Close" in level_1:
                close_series = raw[ticker]["Close"]
            elif "Close" in level_0 and ticker in level_1:
                close_series = raw["Close"][ticker]

            if close_series is None:
                print(f"Warning: no close price data found for {ticker}")
                continue

            close_frames.append(
                close_series.rename("close")
                .reset_index()
                .assign(ticker=ticker)
            )
    else:
        # Single ticker case.
        ticker = tickers[0]
        close_frames.append(
            raw["Close"].rename("close")
            .reset_index()
            .assign(ticker=ticker)
        )

    prices = pd.concat(close_frames, ignore_index=True)
    if "Date" in prices.columns:
        date_col = "Date"
    elif "Datetime" in prices.columns:
        date_col = "Datetime"
    elif "index" in prices.columns:
        date_col = "index"
    else:
        date_col = next(col for col in prices.columns if col not in {"close", "ticker"})
    prices = prices.rename(columns={date_col: "date"})
    prices["date"] = pd.to_datetime(prices["date"]).dt.tz_localize(None)
    prices["close"] = pd.to_numeric(prices["close"], errors="coerce")
    prices = prices.dropna(subset=["date", "close"])
    prices = prices[["date", "ticker", "close"]].sort_values(["ticker", "date"])
    return prices.reset_index(drop=True)


def download_daily_close_prices(contracts: pd.DataFrame) -> pd.DataFrame:
    tickers = sorted(set(contracts["ticker"]).union({MARKET_BENCHMARK}))
    start_date = (contracts["action_date"].min() - pd.Timedelta(days=70)).date()
    end_date = (contracts["action_date"].max() + pd.Timedelta(days=70)).date()

    print(f"Downloading adjusted close prices for: {', '.join(tickers)}")
    raw = yf.download(
        tickers=tickers,
        start=str(start_date),
        end=str(end_date),
        auto_adjust=True,
        group_by="ticker",
        progress=False,
        threads=True,
    )

    prices = extract_close_prices(raw, tickers)
    print("Downloaded daily close prices:", prices.shape)
    return prices


