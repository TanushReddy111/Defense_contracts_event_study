@dataclass
class PriceData:
    frame: pd.DataFrame
    close_by_date: dict[pd.Timestamp, float]


def build_price_lookup(prices: pd.DataFrame) -> dict[str, PriceData]:
    lookup: dict[str, PriceData] = {}
    for ticker, group in prices.groupby("ticker"):
        frame = group.sort_values("date").reset_index(drop=True)
        lookup[ticker] = PriceData(
            frame=frame,
            close_by_date=dict(zip(frame["date"], frame["close"])),
        )
    return lookup


def first_trading_index_on_or_after(price_frame: pd.DataFrame, date: pd.Timestamp) -> int | None:
    dates = price_frame["date"].to_numpy(dtype="datetime64[ns]")
    index = int(np.searchsorted(dates, np.datetime64(date), side="left"))
    if index >= len(price_frame):
        return None
    return index


def safe_return(end_price: float, start_price: float) -> float:
    if start_price is None or end_price is None or start_price <= 0:
        return np.nan
    return (end_price / start_price) - 1


def calculate_event_window_returns(
    contracts: pd.DataFrame,
    prices: pd.DataFrame,
) -> pd.DataFrame:
    price_lookup = build_price_lookup(prices)
    benchmark = price_lookup.get(MARKET_BENCHMARK)
    if benchmark is None:
        raise ValueError(f"Benchmark ticker {MARKET_BENCHMARK} is missing from price data.")

    rows = []
    for _, event in contracts.iterrows():
        ticker = event["ticker"]
        ticker_prices = price_lookup.get(ticker)
        if ticker_prices is None:
            continue

        event_index = first_trading_index_on_or_after(ticker_prices.frame, event["action_date"])
        if event_index is None:
            continue

        event_trading_date = ticker_prices.frame.loc[event_index, "date"]
        event_close = ticker_prices.frame.loc[event_index, "close"]
        benchmark_event_close = benchmark.close_by_date.get(event_trading_date)
        if benchmark_event_close is None:
            continue

        for window in EVENT_WINDOWS:
            pre_index = event_index - window
            post_index = event_index + window
            if pre_index < 0 or post_index >= len(ticker_prices.frame):
                continue

            pre_date = ticker_prices.frame.loc[pre_index, "date"]
            post_date = ticker_prices.frame.loc[post_index, "date"]

            ticker_pre_close = ticker_prices.frame.loc[pre_index, "close"]
            ticker_post_close = ticker_prices.frame.loc[post_index, "close"]
            benchmark_pre_close = benchmark.close_by_date.get(pre_date)
            benchmark_post_close = benchmark.close_by_date.get(post_date)

            if benchmark_pre_close is None or benchmark_post_close is None:
                continue

            pre_stock_return = safe_return(event_close, ticker_pre_close)
            post_stock_return = safe_return(ticker_post_close, event_close)
            pre_market_return = safe_return(benchmark_event_close, benchmark_pre_close)
            post_market_return = safe_return(benchmark_post_close, benchmark_event_close)

            rows.append(
                {
                    "event_id": event["event_id"],
                    "year": event["year"],
                    "ticker": ticker,
                    "company_name": event.get("company_name"),
                    "action_date": event["action_date"],
                    "event_trading_date": event_trading_date,
                    "window_trading_days": window,
                    "plain_english_window": readable_window(window),
                    "transaction_amount": event["transaction_amount"],
                    "pre_stock_return": pre_stock_return,
                    "post_stock_return": post_stock_return,
                    "pre_market_return": pre_market_return,
                    "post_market_return": post_market_return,
                    "pre_abnormal_return": pre_stock_return - pre_market_return,
                    "post_abnormal_return": post_stock_return - post_market_return,
                }
            )

    returns = pd.DataFrame(rows)
    if returns.empty:
        raise ValueError("No event-window returns could be calculated.")
    return returns


def calculate_relative_day_panel(
    contracts: pd.DataFrame,
    prices: pd.DataFrame,
) -> pd.DataFrame:
    price_lookup = build_price_lookup(prices)
    benchmark = price_lookup.get(MARKET_BENCHMARK)
    if benchmark is None:
        raise ValueError(f"Benchmark ticker {MARKET_BENCHMARK} is missing from price data.")

    rows = []
    for _, event in contracts.iterrows():
        ticker = event["ticker"]
        ticker_prices = price_lookup.get(ticker)
        if ticker_prices is None:
            continue

        event_index = first_trading_index_on_or_after(ticker_prices.frame, event["action_date"])
        if event_index is None:
            continue

        min_required_index = event_index + min(RELATIVE_DAY_RANGE) - 1
        max_required_index = event_index + max(RELATIVE_DAY_RANGE)
        if min_required_index < 0 or max_required_index >= len(ticker_prices.frame):
            continue

        event_rows = []
        running_car = 0.0
        for relative_day in RELATIVE_DAY_RANGE:
            current_index = event_index + relative_day
            previous_index = current_index - 1

            current_date = ticker_prices.frame.loc[current_index, "date"]
            previous_date = ticker_prices.frame.loc[previous_index, "date"]
            current_close = ticker_prices.frame.loc[current_index, "close"]
            previous_close = ticker_prices.frame.loc[previous_index, "close"]

            benchmark_current_close = benchmark.close_by_date.get(current_date)
            benchmark_previous_close = benchmark.close_by_date.get(previous_date)
            if benchmark_current_close is None or benchmark_previous_close is None:
                continue

            stock_return = safe_return(current_close, previous_close)
            market_return = safe_return(benchmark_current_close, benchmark_previous_close)
            abnormal_return = stock_return - market_return
            running_car += abnormal_return

            event_rows.append(
                {
                    "event_id": event["event_id"],
                    "year": event["year"],
                    "ticker": ticker,
                    "company_name": event.get("company_name"),
                    "action_date": event["action_date"],
                    "relative_day": relative_day,
                    "trading_date": current_date,
                    "transaction_amount": event["transaction_amount"],
                    "stock_return": stock_return,
                    "market_return": market_return,
                    "abnormal_return": abnormal_return,
                    "car": running_car,
                }
            )

        rows.extend(event_rows)

    panel = pd.DataFrame(rows)
    if panel.empty:
        raise ValueError("No relative-day event panel could be calculated.")

    panel = panel.sort_values(["event_id", "relative_day"]).reset_index(drop=True)
    post_mask = panel["relative_day"] >= 0
    panel["post_car"] = np.nan
    panel.loc[post_mask, "post_car"] = panel.loc[post_mask].groupby("event_id")[
        "abnormal_return"
    ].cumsum()
    return panel


