def post_usaspending(payload: dict, max_retries: int = 4) -> dict:
    """POST to USAspending with retries and a User-Agent header."""
    url = "https://api.usaspending.gov/api/v2/search/spending_by_transaction/"
    headers = {
        "Accept": "application/json",
        "Content-Type": "application/json",
        "User-Agent": "contract-market-analysis-student-project/1.0",
    }

    for attempt in range(1, max_retries + 1):
        try:
            response = requests.post(url, json=payload, headers=headers, timeout=60)
            response.raise_for_status()
            return response.json()
        except requests.RequestException as exc:
            if attempt == max_retries:
                raise RuntimeError(f"USAspending API failed after {max_retries} attempts") from exc
            sleep_seconds = 2 ** attempt
            print(f"USAspending request failed on attempt {attempt}; retrying in {sleep_seconds}s")
            time.sleep(sleep_seconds)

    raise RuntimeError("USAspending request failed unexpectedly")


def scrape_company_year_contracts(company: dict, year: int) -> list[dict]:
    rows: list[dict] = []
    print(f"Scraping {year} contracts for {company['ticker']} ({company['company_query']})")

    for page in range(1, MAX_PAGES_PER_COMPANY_YEAR + 1):
        payload = {
            "filters": {
                "recipient_search_text": [company["company_query"]],
                "time_period": [
                    {
                        "start_date": f"{year}-01-01",
                        "end_date": f"{year}-12-31",
                    }
                ],
                "award_type_codes": CONTRACT_AWARD_TYPE_CODES,
            },
            "fields": TRANSACTION_FIELDS,
            "page": page,
            "limit": CONTRACTS_PER_PAGE,
            "sort": "Transaction Amount",
            "order": "desc",
        }

        data = post_usaspending(payload)
        page_results = data.get("results", [])
        if not page_results:
            break

        for item in page_results:
            item["ticker"] = company["ticker"]
            item["company_query"] = company["company_query"]
            item["company_name"] = company["company_name"]
            item["query_year"] = year
        rows.extend(page_results)

        page_metadata = data.get("page_metadata", {})
        if not page_metadata.get("hasNext"):
            break

        time.sleep(0.25)

    return rows


def scrape_contract_events() -> pd.DataFrame:
    all_rows: list[dict] = []
    for year in YEARS:
        for company in PUBLIC_COMPANIES:
            all_rows.extend(scrape_company_year_contracts(company, year))

    if not all_rows:
        raise ValueError("No contract events were returned. Check internet access and API settings.")

    contracts = pd.DataFrame(all_rows)
    contracts = contracts.rename(columns={col: snake_case_column(col) for col in contracts.columns})

    contracts["action_date"] = pd.to_datetime(contracts["action_date"], errors="coerce")
    contracts["transaction_amount"] = pd.to_numeric(
        contracts["transaction_amount"], errors="coerce"
    )
    contracts = contracts.dropna(subset=["action_date", "transaction_amount", "ticker"])
    contracts["year"] = contracts["action_date"].dt.year
    contracts = contracts[contracts["year"].isin(YEARS)].copy()

    if "award_id" in contracts.columns:
        contracts["award_id"] = contracts["award_id"].where(
            contracts["award_id"].notna(), ""
        )
        contracts["award_id"] = contracts["award_id"].astype(str).str.strip()
        valid_award_id = (
            contracts["award_id"].ne("")
            & ~contracts["award_id"].str.lower().isin(["nan", "none", "null"])
        )

        award_events = (
            contracts[valid_award_id]
            .sort_values(
                ["ticker", "award_id", "action_date", "transaction_amount"],
                ascending=[True, True, True, False],
            )
            .drop_duplicates(subset=["ticker", "award_id"], keep="first")
        )
        missing_award_events = contracts[~valid_award_id].copy()
        contracts = pd.concat([award_events, missing_award_events], ignore_index=True)

    dedupe_cols = [
        col
        for col in ["generated_internal_id", "internal_id", "action_date", "ticker"]
        if col in contracts.columns
    ]
    if dedupe_cols:
        contracts = contracts.drop_duplicates(subset=dedupe_cols)

    contracts = contracts[contracts["transaction_amount"] >= MIN_TRANSACTION_AMOUNT_USD].copy()
    contracts = contracts.sort_values(
        ["year", "ticker", "transaction_amount"], ascending=[True, True, False]
    )
    contracts["event_id"] = np.arange(1, len(contracts) + 1)

    ordered_columns = [
        "event_id",
        "year",
        "ticker",
        "company_name",
        "company_query",
        "action_date",
        "transaction_amount",
        "recipient_name",
        "award_id",
        "awarding_agency",
        "awarding_sub_agency",
        "award_type",
        "transaction_description",
        "generated_internal_id",
        "internal_id",
    ]
    ordered_columns = [col for col in ordered_columns if col in contracts.columns]
    remaining_columns = [col for col in contracts.columns if col not in ordered_columns]
    return contracts[ordered_columns + remaining_columns].reset_index(drop=True)


