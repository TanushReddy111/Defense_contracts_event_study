def build_window_summary(window_returns: pd.DataFrame) -> pd.DataFrame:
    summary = (
        window_returns.groupby(["year", "window_trading_days", "plain_english_window"], as_index=False)
        .agg(
            number_of_events=("event_id", "nunique"),
            avg_contract_amount=("transaction_amount", "mean"),
            avg_pre_abnormal_return=("pre_abnormal_return", "mean"),
            median_pre_abnormal_return=("pre_abnormal_return", "median"),
            pre_abnormal_return_std=("pre_abnormal_return", "std"),
            avg_post_abnormal_return=("post_abnormal_return", "mean"),
            median_post_abnormal_return=("post_abnormal_return", "median"),
            post_abnormal_return_std=("post_abnormal_return", "std"),
            percent_events_with_positive_post_abnormal_return=(
                "post_abnormal_return",
                lambda s: float((s > 0).mean() * 100),
            ),
        )
        .sort_values(["year", "window_trading_days"])
    )

    n = summary["number_of_events"].replace(0, np.nan)
    summary["pre_abnormal_return_standard_error"] = summary["pre_abnormal_return_std"] / np.sqrt(n)
    summary["post_abnormal_return_standard_error"] = summary["post_abnormal_return_std"] / np.sqrt(n)
    summary["avg_pre_abnormal_return_t_stat"] = (
        summary["avg_pre_abnormal_return"] / summary["pre_abnormal_return_standard_error"]
    )
    summary["avg_post_abnormal_return_t_stat"] = (
        summary["avg_post_abnormal_return"] / summary["post_abnormal_return_standard_error"]
    )
    summary["avg_pre_abnormal_return_ci_lower"] = (
        summary["avg_pre_abnormal_return"]
        - NORMAL_95_CRITICAL_VALUE * summary["pre_abnormal_return_standard_error"]
    )
    summary["avg_pre_abnormal_return_ci_upper"] = (
        summary["avg_pre_abnormal_return"]
        + NORMAL_95_CRITICAL_VALUE * summary["pre_abnormal_return_standard_error"]
    )
    summary["avg_post_abnormal_return_ci_lower"] = (
        summary["avg_post_abnormal_return"]
        - NORMAL_95_CRITICAL_VALUE * summary["post_abnormal_return_standard_error"]
    )
    summary["avg_post_abnormal_return_ci_upper"] = (
        summary["avg_post_abnormal_return"]
        + NORMAL_95_CRITICAL_VALUE * summary["post_abnormal_return_standard_error"]
    )

    for col in [
        "avg_pre_abnormal_return",
        "median_pre_abnormal_return",
        "pre_abnormal_return_standard_error",
        "avg_pre_abnormal_return_ci_lower",
        "avg_pre_abnormal_return_ci_upper",
        "avg_post_abnormal_return",
        "median_post_abnormal_return",
        "post_abnormal_return_standard_error",
        "avg_post_abnormal_return_ci_lower",
        "avg_post_abnormal_return_ci_upper",
    ]:
        summary[f"{col}_percent"] = pct(summary[col])

    return summary.reset_index(drop=True)


def build_company_contract_totals(contracts: pd.DataFrame) -> pd.DataFrame:
    return (
        contracts.groupby(["year", "ticker", "company_name"], as_index=False)
        .agg(
            contract_event_count=("event_id", "nunique"),
            total_contract_transaction_amount=("transaction_amount", "sum"),
            average_contract_transaction_amount=("transaction_amount", "mean"),
        )
        .sort_values(["year", "total_contract_transaction_amount"], ascending=[True, False])
        .reset_index(drop=True)
    )


def build_year_comparison(window_summary: pd.DataFrame) -> pd.DataFrame:
    comparison = window_summary.pivot_table(
        index=["window_trading_days", "plain_english_window"],
        columns="year",
        values=[
            "avg_post_abnormal_return_percent",
            "avg_post_abnormal_return_t_stat",
            "avg_post_abnormal_return_ci_lower_percent",
            "avg_post_abnormal_return_ci_upper_percent",
            "median_post_abnormal_return_percent",
            "percent_events_with_positive_post_abnormal_return",
            "number_of_events",
        ],
        aggfunc="first",
    )
    comparison.columns = [f"{metric}_{year}" for metric, year in comparison.columns]
    comparison = comparison.reset_index()

    start_year = min(YEARS)
    end_year = max(YEARS)
    start_col = f"avg_post_abnormal_return_percent_{start_year}"
    end_col = f"avg_post_abnormal_return_percent_{end_year}"
    if start_col in comparison.columns and end_col in comparison.columns:
        comparison[f"avg_post_abnormal_return_percent_change_{end_year}_minus_{start_year}"] = (
            comparison[end_col] - comparison[start_col]
        )

    return comparison.sort_values("window_trading_days").reset_index(drop=True)


def build_market_efficiency_summary(relative_day_panel: pd.DataFrame) -> pd.DataFrame:
    post_panel = relative_day_panel[relative_day_panel["relative_day"] >= 0].copy()
    rows = []

    for event_id, group in post_panel.groupby("event_id"):
        group = group.sort_values("relative_day")
        final_row = group[group["relative_day"] == 21]
        if final_row.empty:
            continue

        final_post_car = float(final_row["post_car"].iloc[0])
        if math.isclose(final_post_car, 0.0, abs_tol=1e-9):
            first_50pct_day = np.nan
        else:
            direction = np.sign(final_post_car)
            threshold = abs(final_post_car) * 0.50
            reflected = group[
                (np.sign(group["post_car"]) == direction)
                & (group["post_car"].abs() >= threshold)
            ]
            first_50pct_day = (
                float(reflected["relative_day"].iloc[0]) if not reflected.empty else np.nan
            )

        positive_days = group[group["abnormal_return"] > 0]
        first_positive_abnormal_day = (
            float(positive_days["relative_day"].iloc[0]) if not positive_days.empty else np.nan
        )

        rows.append(
            {
                "event_id": event_id,
                "year": int(group["year"].iloc[0]),
                "ticker": group["ticker"].iloc[0],
                "final_21_day_post_car": final_post_car,
                "first_50pct_reflection_day": first_50pct_day,
                "first_positive_abnormal_day": first_positive_abnormal_day,
            }
        )

    event_efficiency = pd.DataFrame(rows)
    if event_efficiency.empty:
        raise ValueError("Could not build market efficiency summary.")

    yearly_efficiency = (
        event_efficiency.groupby("year", as_index=False)
        .agg(
            number_of_events=("event_id", "nunique"),
            avg_21_day_post_car=("final_21_day_post_car", "mean"),
            median_21_day_post_car=("final_21_day_post_car", "median"),
            avg_first_50pct_reflection_day=("first_50pct_reflection_day", "mean"),
            median_first_50pct_reflection_day=("first_50pct_reflection_day", "median"),
            avg_first_positive_abnormal_day=("first_positive_abnormal_day", "mean"),
            median_first_positive_abnormal_day=("first_positive_abnormal_day", "median"),
            percent_reflected_by_day_1=(
                "first_50pct_reflection_day",
                lambda s: float((s <= 1).mean() * 100),
            ),
            percent_reflected_by_day_5=(
                "first_50pct_reflection_day",
                lambda s: float((s <= 5).mean() * 100),
            ),
        )
    )
    yearly_efficiency["avg_21_day_post_car_percent"] = pct(
        yearly_efficiency["avg_21_day_post_car"]
    )
    yearly_efficiency["median_21_day_post_car_percent"] = pct(
        yearly_efficiency["median_21_day_post_car"]
    )
    return event_efficiency, yearly_efficiency


